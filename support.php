            <h4>What is hot-desking? </h4>
            <p>Hot-desking is an organizational workspace system in which desks are used by different people at different times, on an ad hoc basis. Typically, the aim is to maximize space efficiency and lessen real estate risk by reducing redundant office space. Hot desks can be implemented in private offices as well as coworking spaces. The flexibility associated with hot-desking is often linked to an increase in employee innovation, while the social aspect is cited as a way to encourage cross-disciplinary collaboration.</p>
          </div>

          <div class="box">
          <img src="images/icon-process.png" alt="">  
            <h4>How does hot-desking work?</h4>
            <p>Find a desk, plug in, and get to work: Hot desks provide space for professionals to work as needed, in both private and shared office spaces. That means that the particulars for hot-desking may vary depending on the environment, but here are the primary factors to consider.  </p>
          </div>

          <div class="box">
          <img src="images/icon-power.png" alt="">  
            <h4>Access to power outlets and phones</h4>
            <p>Hot desks typically come with their own power outlets and WiFi connection. In shared coworking spaces like WeWork, there are dedicated spaces away from the desk for taking calls—phone booths or in shared lounges, for example. In private office environments, particularly in sales companies, a permanent phone is likely attached to the desk for different employees to use as needed. </p>
          </div>
        </div>

        <div class="services_boxes">
          <div class="box">
            <img src="images/icon-meeting.png" alt="">  
            <h4>Bookable meeting rooms</h4>
            <p>Most hot-desking arrangements in shared coworking spaces allow you to book meeting rooms through an app or a web log-in. Amenity-rich solutions like WeWork also afford you access to shared lounges, kitchens, brainstorming areas, conference rooms, and a global network of professionals. </p>
          </div>

          <div class="box">
          <img src="images/icon-internet.png" alt="">  
            <h4>Technology and internet</h4>
            <p>Hot-desking wouldn’t be possible if it weren’t for laptops, tablets, and smartphones—and all offices with hot-desking arrangements should provide WiFi to accommodate. Some users, however, need more than their laptop to work effectively. For this reason, some hot desks come with a monitor and a VGA, DVI, or HDMI connection, allowing users to plug-in and use a second screen temporarily.  </p>
          </div>

          <div class="box">
          <img src="images/icon-room.png" alt="">  
            <h4>Private space when needed </h4>
            <p>Most professions call for some privacy during the workday, whether it’s for attending meetings, strategic planning, or discussing finances. These conversations are likely too sensitive for a hot desk, especially when you’re surrounded by people from other teams or other companies, and require a more private space.</p>
          </div>
        </div>
